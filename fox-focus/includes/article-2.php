            <p>Hodge credits former Temple University President Peter J. Liacouras for his focus on creating an oncampus community.</p>
            <p>The shift in the environment on campus is evident by the ways communication has evolved. Before social media and texting, Roseman shared how students gathered together and spread the word about what was happening on campus.</p>
            <p>"There were these huge columns on campus and people used to staple event flyers and that's how you knew about events. We didn't have cell phones. We just used to knock on people's doors and say, ‘Do you want to hang out?' That worked for us."</p>
