            <p>Hodge, <em>LAW '74, '79</em>, and Roseman, <em>STH '02</em>, <em>MS '18</em>, have seen the campus change from various of perspectives. For Hodge, it was first as a student, then an educator, then a parent. He enrolled in Temple Law School in 1971 and has been a pillar in the Temple University community ever since. He's taught at the Fox School, the Lewis Katz School of Medicine and the Beasley School of Law. At Temple, he led one of the first large classroom lectures and served as chair of the Department of Legal Studies for 25 years. Hodge's daughter Lori and son both attended Temple, receiving two degrees each.</p>
            <div class="article-2__container-quote">"NOW IT'S A COMPLETELY DIFFERENT TYPE OF SCHOOL, TO THE POINT WHERE EVERYBODY WANTS TO LIVE ON CAMPUS<br><img src="https://www2.fox.temple.edu/fox-focus/assets/img/quote-close-white.png"></div>
            
            <p>Roseman has been a student, staff member, educator and possibly a parent at Temple, as her children are expected to attend the university. When she first graduated from the School of Sport, Tourism and Hospitality Management, she did not expect to return as a staff member. "My background was in event planning and never in a million years did I see myself going and then sticking with higher education," she says. Now celebrating 15 years with the Fox School, it seems to be the perfect fit. "I have a true passion for working in the Honors program," she adds.</p>
            
            <p>Together, Hodge and Roseman have a combined total of 76 years at Temple University. Here's what they said has changed the most.</p>
           
