        <p>The experience of living on campus has evolved.</p>
        <p>"When I was a student, Temple was very much a commuter school," Roseman recalls. "There were very few dorms to choose from and nobody was living in off-campus housing as a freshman. Now it's a completely different type of school, to the point where everybody wants to live on campus."</p>
