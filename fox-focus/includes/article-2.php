            <p><strong>JON HUYNH, <em>BBA '16</em></strong>, was a Fox Honors student and credits Roseman for guiding him. "She definitely helped me figure out what I wanted to do. She did a lot of my advising and because of her, I ended up working as a student advisor," he says.</p>
            <p>As both Roseman and Huynh discussed their time as students, it was clear there were differences. One of the first points of conversation: the food.</p>
            <p>Roseman recalls only a handful of spots students could eat and socialize besides the Johnson and Hardwick cafeteria and dorms. "There was one pizza place called Owl's Nest and we would hang out at Salad Alley, where Maxi's is now located."</p>
            <p>Students today can now enjoy a variety of selections, from food trucks to fast food restaurants in the student center. It is also something that students miss when they graduate.</p>
            <p>"I didn't realize it until I moved away for my first job, but food trucks and having such a vibrant food scene is not common everywhere you go," Huynh shares.</p> 
            <p>During Roseman's time as a student, the most popular majors were nursing, communication and education. On the contrary, in 2016, Huynh's peers were interested in management information systems, actuarial science and risk management. Huynh himself was part of the first graduating class for the statistical science and data analytics program.</p> 
            <p>"I thought there was one path to do everything, but the Fox School and Temple opened up my eyes to see that not every job, career, or whatever you want to do, has just one way to get there," he says. "It was through opportunities like the BYOBB [the Institute for Innovation and Entrepreneurship's Be Your Own Boss Bowl®] that I started to understand that there are all sorts of tools here for students interested in starting their own thing. Joining that competition helped me understand that everything doesn't have to be unilateral," Huynh says. </p>
            <p>The BYOBB is a pitch competition for aspiring entrepreneurs, open to all students, alumni, faculty and staff from all 17 schools and colleges within Temple. Huynh participated in 2019 and won the "Crowd Favorite" award. </p>
            <p>Now, Huynh is pursuing a graduate degree in data science at Columbia University while working as a software engineer for mobile iOS development at All Your Cards In One. It is a startup mobile app that combines your cards so that each transaction routes to the best credit card in your wallet when you pay. </p>
            <p>"I think I became a professional," Huynh says, considering his time at the Fox School. "Coming in, I had no idea what it meant to be ready for job fairs, career fairs and interviews or how to work in teams and in the workplace. The school helped prepare me for all those things at a professional level."</p>
