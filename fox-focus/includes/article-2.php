                <p>"I truly believe that both of my degrees have helped prepare me for who I am today," Roseman shares. "Over the years, I've developed a lot more confidence. I've expanded my network. I'm a true student leader, an advocate and have built very strong and lasting relationships with my students. 
                    <p>"Another thing that I've learned is that empathy is everything because you really never truly know what someone is going through. Always be supportive, compassionate and ready to listen." 
                    <p>This year, Hodge celebrates his 47th year of teaching. "It dawned on me how long I have been at Temple when a student came up to me and said, ‘My mom said to say hi," he says, laughing. "I have gone through generations of students at Temple." 
                    <p>Hodge will be transitioning to retirement over the next few years. However, he is proud of his accomplishments and the work he has pioneered during his time at Temple. 
                    <p>Roseman adds, "I believe that Temple is on a path to continuously improving campus. I see Temple aiming to be a very vibrant campus amongst its competitors that are maybe not in an urban environment. I don't ever see it going backward. I only see it continuing to move forward."</p>
