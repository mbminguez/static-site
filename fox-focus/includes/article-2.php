            <p>Roseman adds that the diversification of majors and departments is due to the Fox School's commitment to its four Strategic Plan pillars, including Education Innovation.</p>
            <p>"Fox evolves with modern trends," she says.</p> 
            <p>Inside the classroom also looks increasingly different. Instructors now use smart boards instead of slide projectors. Recently, students in graduate courses have embraced virtual reality (VR), taking classes via Oculus VR headsets.</p>
            <div class="article-2__container-quote">"I'VE WITNESSED THE TRANSFORMATION TO SUCH A DIVERSE POPULATION OF STUDENTS FROM ALL AROUND THE WORLD.<br><img src="https://www2.fox.temple.edu/fox-focus/assets/img/quote-close-white.png"></div>
            <p>"Today, we have smart classrooms and it's amazing how we've advanced from a technological point of view," Hodge adds.</p>
            <p>Roseman echoes her father's excitement. "I never imagined any scenario in higher education where you would be able to do everything virtually." </p>
            <p>That virtual expansion increased throughout the pandemic. "Education is going to be changed forever," Hodge elaborates. "There's always going to be a demand for online courses."</p>
        </div>
    </div>

<div class="article-2__container article-2__container-narrow article-2__container-narrow-accent">
    <div class="article-2__content article-2__content-title article-2__content-title-l"> DIVERSITY AND INCLUSIVITY FOR STUDENTS
    </div>
    <p>Both Hodge and Roseman have seen firsthand how Temple students have changed and evolved.</p> 
    <p>"I've witnessed the transformation to such a diverse population of students from around the world," says Hodge.</p> 
    <p>Students are not just growing in diversity, but also in their desire for a well-rounded higher education experience.</p> 
    <p>"The students are different today. They won't just accept what I say. They'll challenge you on something, whereas before if the teacher said it, that was gospel," says Hodge.</p> 
    <p>Roseman agrees, "I believe that the hunger and the drive have always been there, but now students are more focused on leadership positions and experiential learning opportunities early. More students want to study abroad, not just for a summer, but for a semester or even for an entire year."</p>
    <p>"Diversity, inclusion and social responsibility are much more important to students now and they really are showing greater emotional and cultural intelligence," Roseman adds. Temple has also made a significant stride in available resources for the community on campus.</p>
    <p>"The university has made a much more conscious effort to support the students and staff at the school and university as a whole—with medical, psychological, emotional and financial help," Hodge says.</p>
